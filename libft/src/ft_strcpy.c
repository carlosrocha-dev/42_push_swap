#include "../libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t	i;
	size_t	src_len;

	src_len = ft_strlen(src);
	if (src_len >= (size_t)(dest - src))
		return (NULL);
	i = 0;
	while (i < src_len)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
